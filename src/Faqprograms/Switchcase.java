package Faqprograms;

public class Switchcase {
	
	public static void main(String[] args) {
		
		 int week=8;
		switch (week) {
		case 1: 
			System.out.println("the day is monday day");break;
			
		case 2: 
			System.out.println("this day is  tuesday");break;
		
		case 3: 
			System.out.println("this day is  wensday");break;
		
		case 4: 
			System.out.println("this day is thursday");break;
		
		case 5: 
			System.out.println("this day is friday");break;
		
		case 6: 
			System.out.println("this day is satday");break;
			
		case 7: 
			System.out.println("this day is sunday");break;		
		
		default:
			System.out.println("this has no day");
		}
	}

}
